    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         Person newPerson =  createPersonInformation();
        if(newPerson==null)
            JOptionPane.showMessageDialog(null, "Information cannot be created!");
        if(pList.Contains(newPerson))
            JOptionPane.showMessageDialog(null,"Information already exists!");
        if(!(newPerson==null)&&(!(pList.Contains(newPerson))))
            pList.addPerson(newPerson);
            numofperson++;
            position = numofperson-1;
            serializeperson_List();
            populate_TextBoxes(pList.get(position),position+1);
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
       Person newPerson =  createPersonInformation();
        if(newPerson==null)
                JOptionPane.showMessageDialog(null,"Information cannot be created!");
        if(pList.Contains(newPerson) && position != pList.getIndex(newPerson) )
                JOptionPane.showMessageDialog(null,"Information already exists!");
        if(!(newPerson==null) && (!pList.Contains(newPerson)) && position ==pList.getIndex(newPerson) )
            pList.updatePerson(position,newPerson);
        if(!pList.isEmpty()){
            serializeperson_List();
    }//GEN-LAST:event_jButton6ActionPerformed
    }
    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
         if(!pList.isEmpty()){
            pList.deletePerson(position);
            serializeperson_List();
            numofperson--;
            if(position == numofperson)
               position-- ;
            if(pList.isEmpty())
                Clear_TextBoxes();
            else
                populate_TextBoxes(pList.get(position),position+1); 
        }
    }//GEN-LAST:event_jButton7ActionPerformed

