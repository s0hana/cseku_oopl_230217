    public void deserializeperson_List(){
    
    try{
      
     FileInputStream fin = new FileInputStream("Person.ser");
     ObjectInputStream ois = new ObjectInputStream(fin);
           
     pList = (Person_List) ois.readObject();
     numofperson = pList.num_Person();
     ois.close();
    
     //return item;
     
    }
    catch(Exception e){
     JOptionPane.showMessageDialog(null,e.getMessage());
     
    } 
   }
public void serializeperson_List(){
    
    try{
      
      FileOutputStream fout = new FileOutputStream("Person.ser");
      ObjectOutputStream oos = new ObjectOutputStream(fout);   
      oos.writeObject(pList);
      oos.close();
      //JOptionPane.showMessageDialog(null,"Successfully saved");
      
    }
    catch(Exception e){
      JOptionPane.showMessageDialog(null,e.getMessage());
    }
  }
   private Person createPersonInformation()
   {
      if(this.jTextField1.getText().isEmpty())
      {
          return null;
      }
     String name =  this.jTextField1.getText().trim();
     String road =  this.jTextField2.getText().trim();
     String post =  this.jTextField3.getText().trim();
     String dis =  this.jTextField4.getText().trim();
     return new Person(name, road, post, dis);
     
   }
   private void populate_TextBoxes(Person p, int position)
   {
       Address address = p.address;
       this.jTextField1.setText(p.getName());
       this.jTextField2.setText(address.getRoad());
       this.jTextField3.setText(address.getPost());
       this.jTextField4.setText(address.getDistrict());
       this.jTextField5.setText(position+"/"+numofperson);
   }
   private void Clear_TextBoxes()
   {
       this.jTextField1.setText("");
       this.jTextField2.setText("");
       this.jTextField3.setText("");
       this.jTextField4.setText("");
       this.jTextField5.setText("");
   }
    
    
