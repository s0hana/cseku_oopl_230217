    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        Student newStudent =  createStudentInformation();
        if(newStudent==null)
            JOptionPane.showMessageDialog(null, "Information cannot be created!");
        if(sList.Contains(newStudent))
            JOptionPane.showMessageDialog(null,"Information already exists!");
        if(!(newStudent==null)&&(!(sList.Contains(newStudent))))
            sList.addStudent(newStudent);
            numofstd++;
            position = numofstd-1;
            serializestd_List();
            populate_TextBoxes(sList.get(position),position+1);
                
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
         Student newStudent =  createStudentInformation();
        if(newStudent==null)
                JOptionPane.showMessageDialog(null,"Information cannot be created!");
        if(sList.Contains(newStudent) && position != sList.getIndex(newStudent) )
                JOptionPane.showMessageDialog(null,"Information already exists!");
        if(!(newStudent==null) && (!sList.Contains(newStudent)) && position ==sList.getIndex(newStudent) )
            sList.updateStudent(position,newStudent);
        if(!sList.isEmpty()){
            serializestd_List();
            
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        if(!sList.isEmpty()){
            sList.deleteStudent(position);
            serializestd_List();
            numofstd--;
            if(position == numofstd)
               position-- ;
            if(sList.isEmpty())
                Clear_TextBoxes();
            else
                populate_TextBoxes(sList.get(position),position+1); 
        }
    }//GEN-LAST:event_jButton7ActionPerformed

