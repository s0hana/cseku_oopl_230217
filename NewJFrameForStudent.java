public void deserializestd_List(){
    
    try{
      
     FileInputStream fin = new FileInputStream("Student.ser");
     ObjectInputStream ois = new ObjectInputStream(fin);
           
     sList = (Student_List) ois.readObject();
     numofstd = sList.num_Students();
     ois.close();
    
     //return item;
     
    }
    catch(Exception e){
     JOptionPane.showMessageDialog(null,e.getMessage());
     
    } 
   }
public void serializestd_List(){
    
    try{
      
      FileOutputStream fout = new FileOutputStream("Student.ser");
      ObjectOutputStream oos = new ObjectOutputStream(fout);   
      oos.writeObject(sList);
      oos.close();
      //JOptionPane.showMessageDialog(null,"Successfully saved");
      
    }
    catch(Exception e){
      JOptionPane.showMessageDialog(null,e.getMessage());
    }
  }
  private Student  createStudentInformation()
  {
      if(this.jTextField1.getText().isEmpty()||this.jTextField2.getText().isEmpty()||this.jTextField3.getText().isEmpty()||this.jTextField4.getText().isEmpty()||this.jTextField5.getText().isEmpty())
          return null;
      String name = this.jTextField1.getText().trim();
      String road = this.jTextField2.getText().trim();
      String post = this.jTextField3.getText().trim();
      String district = this.jTextField4.getText().trim();
      String id = this.jTextField5.getText().trim();
      try{
          int stdid = Integer.parseInt(id);
         return new Student(stdid, name, road, post,  district);
      }catch(Exception e)
      {
          System.out.println(e);
          return null;
      }
  }
   private void populate_TextBoxes(Student std, int position)
   {
        Address address = std.address; 
       this.jTextField1.setText(std.getName());
       this.jTextField2.setText(address.getRoad());
       this.jTextField3.setText(address.getPost());
       this.jTextField4.setText(address.getDistrict());
       this.jTextField5.setText(Integer.toString(std.getId()));
       this.jTextField6.setText(position+"/"+numofstd);
               
   }
